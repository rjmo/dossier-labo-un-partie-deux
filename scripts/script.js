function montrer(idElem) {
  document.getElementById(idElem).style.display = "block";
}
function cacher(idElem) {
  document.getElementById(idElem).style.display = "none";
}
function pause(idvideo) {
  document.getElementById(idvideo).pause();
}
function play(idvideo) {
  document.getElementById(idvideo).play();
}
